    private void tfdSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfdSalvarActionPerformed
        // TODO add your handling code here:
        
        if ( validateFields() ){
            String payMethodValue = tfdPayMethod.getText();
            String limite = tfdLimite.getText();
            int intLimite = Integer.parseInt(limite);
            
            erpsystem.model.PayMethod payMethod = new erpsystem.model.PayMethod();
            payMethod.setDescricao(payMethodValue);
            payMethod.setLimite(intLimite);
            PayMethod payMethodModel = new PayMethod();
            
            if (! payMethodModel.exists(payMethodValue) )
            	payMethodModel.add(payMethod);
            else
                javax.swing.JOptionPane.showMessageDialog(null, "Forma de pagamento já cadastrada.");
        }
    }//GEN-LAST:event_tfdSalvarActionPerformed

    boolean validateFields()
    {
        if ( tfdPayMethod.getText().trim().equals("") ){
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor, informe o método de pagamento.");
            tfdPayMethod.grabFocus();
            return false;
        }  

        String limite = tfdLimite.getText().trim();
        
        if ( limite.equals("") ){
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor, informe o limite de dias.");
            tfdLimite.grabFocus();
            return false;
        }  
        
        if ( !Util.isInt(limite) ){
            javax.swing.JOptionPane.showMessageDialog(null, "Informe um número válido para o limite.");
            tfdLimite.grabFocus();
            return false;
        }
          
        return true;
    }
    
